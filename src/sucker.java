
public class sucker {
	int IQ;
	double money;

	public sucker(int IQ, double money) {
		this.IQ = IQ;
		this.money = money;
	}

	public int getIQ() {
		return IQ;
	}

	public void setIQ(int iQ) {
		IQ = iQ;
	}

	public double getMoney() {
		return money;
	}

	public void setMoney(double money) {
		this.money = money;
	}
}
